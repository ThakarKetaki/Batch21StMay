package demoPackage;

public class ArithmaticOperator 
{
	public static void main(String[] args) 
	{
		/*Arithmetic Operators
		 * Addition +
		 * Substraction -
		 * Multiplication *
		 * Division /
		 * Modulus %
		 * Increment ++
		 * Decrement --
		 * Assignment =
		 * */
		int a=30;
		int b=11;
		int result=a+b;
		System.out.println(result);
		
		result=a-b;
		System.out.println(result);
		
		result=a*b;
		System.out.println(result);
		
		result=a/b;
		System.out.println(result);
		
		result=a%b;
		System.out.println(result);
	}
	
}
